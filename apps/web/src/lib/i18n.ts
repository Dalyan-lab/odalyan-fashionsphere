interface LocaleState {
  lang: Lang;
  currency: string;
  setLang: (l: Lang) => void;
  setCurrency: (c: string) => void;
}

export const useLocale = create<LocaleState>()(
  persist(
    (set) => ({
      lang: 'fr',
      currency: 'EUR',
      setLang: (lang) => set({ lang }),
      setCurrency: (currency) => set({ currency }),
    }),
    { name: 'odalyan-locale' },
  ),
);

/** Hook de traduction. */
export function useT() {
  const lang = useLocale((s) => s.lang);
  return (key: string) => DICT[lang][key] ?? key;
}

/** Convertit un montant (dans `from`) vers la devise sélectionnée et le formate. */
export function convertAndFormat(amount: number, from: string, currencyCode: string): string {
  const fromCur = CURRENCIES[from] ?? CURRENCIES.EUR!;
  const target = CURRENCIES[currencyCode] ?? CURRENCIES.EUR!;
  const inEur = amount / fromCur.rate;
  const value = inEur * target.rate;

  if (target.code === 'XOF') {
    return `${Math.round(value).toLocaleString(target.locale)} ${target.symbol}`;
  }
  return new Intl.NumberFormat(target.locale, {
    style: 'currency',
    currency: target.code,
    maximumFractionDigits: 2,
  }).format(value);
}
